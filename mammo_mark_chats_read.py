from modules.healthie import Healthie

PAGE_SIZE = 20  # The number of users to request per page (determine the server's limit)
# 20 appears to be the max here

PROD_PROVIDERS = {
    "10410358": "Karam Elabd",
    "10578403": "Tim Herby",
    "10666933": "Bridget Krueger",
    "10578423": "Rakesh Patel",
    "11008105": "Janelle Scachetti",
    "11008180": "Andrea Stage",
    "10578357": "Johann Windt",
}

# FIELDS = ['PROVIDER_ID', 'PROVIDER_NAME', 'RANK', 'ARTICLE', 'COUNTRY']
# FILENAME = 'patient_chat_responses.csv'


def main():
    H = Healthie("PROD")

    provider_counts = {}
    auto_chats = 0
    chats_marked_read = 0

    for provider_id in PROD_PROVIDERS:
        chats = 0
        offset = 0
        all_conversations = []

        print(f"Provider: {provider_id} {PROD_PROVIDERS[provider_id]}")
        print("---------------------------------")

        # Loop until a page returns fewer results than the limit (PAGE_SIZE)
        more_to_get = True

        while more_to_get:
            # Define the variables for the current query
            variables = {
                # "org_chat": True,
                "provider_id": provider_id,
                "page_size": PAGE_SIZE,
                "offset": offset,
            }

            response = H.list_conversationMemberships(variables)

            conversation_memberships = response["conversationMemberships"]

            all_conversations.extend(conversation_memberships)
            print(
                f"Fetched {len(conversation_memberships)} conversation memberships at offset {offset}. Total so far: {len(all_conversations)}"
            )

            if not conversation_memberships:
                print("No conversation memberships to fetch. Loop complete.")
                more_to_get = False

            offset += PAGE_SIZE

        processed_conversations = []

        for conversation in all_conversations:
            conversation_membership_id = conversation["id"]
            conversation_id = conversation["conversation_id"]

            print(
                f"Processing conversation membership {conversation_membership_id} and conversation {conversation_id}"
            )

            if conversation_id in processed_conversations:
                print(f"Conversation {conversation_id} already processed. Skipping.")
                continue

            processed_conversations.append(conversation_id)

            variables = {"conversation_id": conversation["conversation_id"]}

            response = H.get_notes(variables)

            if len(response["notes"]) == 0:
                print("  No notes found")
                continue

            first_note = response["notes"][-1]

            started_by_navigator = first_note["creator"]["id"] == provider_id

            if started_by_navigator:
                chats += 1
                other_responded = False

                is_autogenerated = "your Care Navigator" in first_note["content"]
                print(
                    f"  Started by navigator: {started_by_navigator} - Auto: {is_autogenerated}"
                )
                if is_autogenerated:
                    auto_chats += 1

                    idx = 0
                    for note in response["notes"]:
                        idx += 1
                        responded_by_not_provider = note["creator"]["id"] != provider_id
                        if (not other_responded) and responded_by_not_provider:
                            other_responded = True

                    if not other_responded:
                        print(
                            f"  No other response - Marking {conversation_membership_id} as read"
                        )
                        # mark these read
                        inputs = {"id": conversation_membership_id, "viewed": True}

                        response = H.mark_conversation_read(inputs)

                        print(
                            f"  Marked conversation membership {conversation_membership_id} as read"
                        )
                        print(response)
                        chats_marked_read += 1
                    else:
                        print("  Other response - Not marking read")

        provider_counts[provider_id] = {
            "chats": chats,
            "chats_marked_read": chats_marked_read,
        }

        print("================================")

    for provider_id in provider_counts:
        print(
            f"Provider: {provider_id} {PROD_PROVIDERS[provider_id]:18} - Chats: {provider_counts[provider_id]['chats']:2} - Marked read: {provider_counts[provider_id]['chats_marked_read']:2}"
        )

        # {
        #     "data": {
        #         "conversationMemberships": [
        #         {
        #             "conversation_id": "4304139",
        #             "display_name": "To Mato"
        #         },


if __name__ == "__main__":
    main()
